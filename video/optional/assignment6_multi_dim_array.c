//   - Multi-dimensional arrays

int main() {
  int a[2][2];
  a[0][0] = 0;
  a[0][1] = 1;
  a[1][0] = 2;
  a[1][1] = 3;
  printf("%i\n", a[0][0]); // expected 0
  printf("%i\n", a[0][1]); // expected 1
  printf("%i\n", a[1][0]); // expected 2
  printf("%i\n", a[1][1]); // expected 3
}
