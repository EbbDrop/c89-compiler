// * Assignment 4:
//   - switch statements with case, break, and default

void same_value(int i) {
  switch (i) {
    case 1:
      break;
    case 1:
      break;
  }
}

void muliple_default(int i) {
  switch (i) {
    case 1:
      break;
    default:
      break;
    default:
      break;
  }
}
